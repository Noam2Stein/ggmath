use super::*;

scalar_inner_vectors!(i16(2));

impl Scalar for i16 {}

impl ScalarSigned for i16 {
    fn abs(self) -> Self {
        self.abs()
    }
    fn is_negative(self) -> bool {
        self.is_negative()
    }
    fn is_positive(self) -> bool {
        self.is_positive()
    }
    fn signum(self) -> Self {
        self.signum()
    }
}
